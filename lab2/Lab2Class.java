package lab2Package;

import java.util.Scanner;

public class Lab2Class
{

	public static void main(String[] args)
	{
		// declare variables here
		int numEggs;                                // total number of eggs
		int numDozen;                               // number of dozen eggs
		int numLeftOver;                            // number of eggs left over in a partial dozen
		int numFullCases;                           // number of full cases
		final int NUMEGGSINDOZEN = 12;              // number of eggs in a dozen
		final int NUMDOZENINCASE = 6;               // number of dozen eggs in one case

		Scanner input = new Scanner(System.in);     // create an input stream
		String cleanUpStr;                          // clean up keyboard buffer

		// initialize variables here
		numEggs = 0; // start with 0 eggs
		numDozen = 0;
		numLeftOver = 0;
		cleanUpStr = "none yet";

		// start the rest of your code here

		// print out your name to the console
		System.out.println("Sien Sun");

		// prompt the user to enter the number of eggs
		System.out.print("Enter the total number of eggs\t\t");

		// read in the number of eggs from the keyboard
		numEggs = input.nextInt();

		// clean up the keyboard buffer
		cleanUpStr = input.nextLine();

		// calculate the number of full dozen eggs
		numDozen = numEggs / NUMEGGSINDOZEN;

		// calculate the number of eggs leftover in a partial dozen
		numLeftOver = numEggs % NUMEGGSINDOZEN;

		// calculate the number of full cases
		numFullCases = numDozen / NUMDOZENINCASE;

		// print out the egg information
		System.out.println("Number of Eggs\t\t" + numEggs);
		System.out.println("Complete Dozen Eggs\t" + numDozen);
		System.out.println("Number of Leftover Eggs\t" + numLeftOver);
		System.out.println("Number of Full Cases\t" + numFullCases);

		// close input stream
		input.close();

	} // end main

} // end Lab2Class
